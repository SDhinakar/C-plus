#include<iostream>
using namespace std;
int main()
{
    int x=10,y=6;
    cout<<"x<y  = "<< ((x+1)<(y+6))<<endl;      // Expressions can be used in relational operators
    cout<<"x>y  = "<< (x>y) <<endl;
    cout<<"x<=y = "<< (x<=y)<<endl;
    cout<<"x>=y = "<< (x>=y)<<endl;
    cout<<"x==y = "<< (x==y)<<endl;
    cout<<"x!=y = "<< (x!=y)<<endl;

}